#include "3-calc.h"

/**
 * op_add - function to add two integer
 * @a: first integer
 * @b: second interger
 * Return: the addition of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_div - function to divide two integers
 * @a: first integer
 * @b: second interger
 * Return: the result ofthe division of  two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_sub - function to substract two intigers
 * @a: first integer
 * @b: second interger
 * Return: the difference of two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two numbers
 * @a: first integer
 * @b: second interger
 * Return: the rproduct of two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - function to get the reminder when two integer are divided
 * @a: first integer
 * @b: second interger
 * Return: the reminder of two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
